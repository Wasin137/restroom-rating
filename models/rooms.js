import mongoose, { Schema } from "mongoose";

const roomSchema = new Schema(
    {
  name: {
    type: String,
    required: true,
    unique: true,
    trim: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  },
  updatedAt: {
    type: Date,
    default: Date.now
  }
}, {
  timestamps: { createdAt: 'createdAt', updatedAt: 'updatedAt' }
});

const Room = mongoose.models.Room || mongoose.model('Room', roomSchema);

export default Room;
